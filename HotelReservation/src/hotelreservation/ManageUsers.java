    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       boolean valid=true;
        if(txtId.getText().isEmpty()||txtPassword.getText().isEmpty()||cmbUser.getSelectedItem().toString().isEmpty()||txtConfirmPassword.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Fields can't be blank", "Manage User Form", JOptionPane.ERROR_MESSAGE);
            valid=false;
        }
      else
        {
            if(txtPassword.getText().toString().length()<7)
            {
                JOptionPane.showMessageDialog(null, "Password must have more than 6 characters", "Manage User Form",
                        JOptionPane.ERROR_MESSAGE);
                valid=false;
            }
             else if(!txtPassword.getText().equals(txtConfirmPassword.getText()))
            {
                JOptionPane.showMessageDialog(null, "Password and confirm password should match","Account Manage Form",JOptionPane.ERROR_MESSAGE);
                valid=false;
            }
     if(valid)
                   try
        {
            PreparedStatement stat=con.prepareStatement("Update Users set Password=?, UserType=? where UserID=?");

            
            
            stat.setString(1,(String)txtPassword.getText());
            stat.setString(2,(String)cmbUser.getSelectedItem());
            stat.setString(3,txtId.getText());
            
          
          
            

            int rows=stat.executeUpdate();
            if(rows>0)
            {
                JOptionPane.showMessageDialog(null,"Data Updated..");
            }

        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,"Data not Updated Successully:"+ex,"Users Form",JOptionPane.ERROR_MESSAGE);
        }
              
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

