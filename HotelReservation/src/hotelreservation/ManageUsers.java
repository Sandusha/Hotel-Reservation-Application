    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        try
        {

            PreparedStatement stmt=con.prepareStatement("Select * from Users where UserID=?");

            stmt.setString(1,txtId.getText());

            ResultSet rs=stmt.executeQuery();

            if(rs.next())
            {

                txtId.setText(rs.getString("UserID"));
                txtPassword.setText(rs.getString("Password"));                
                cmbUser.setSelectedItem(rs.getString("UserType"));



                JOptionPane.showMessageDialog(null,"User Found");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"User not Found.");
            }
        }

        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,"Error Encounted while searching:"+ex,"User Form",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnSearchActionPerformed

