    public void update () {
             try
            
        {
         tblRooms.setModel(new DefaultTableModel(null,new String[]{"Room_Number","Type","Bed","Price","Status"})) ;
        
            PreparedStatement stmt = con.prepareStatement("select * from Rooms");
            ResultSet rs=stmt.executeQuery();
            while (rs.next())
            {
               String Room_Number = String.valueOf(rs.getString("RoomNumber"));
                String Type = String.valueOf(rs.getString("RoomType"));
                String Bed = String.valueOf(rs.getString("Bed"));
                String Price = String.valueOf(rs.getString("Price"));
                String Status = String.valueOf(rs.getString("Status"));
                String tbData[] = {Room_Number,Type,Bed,Price,Status}; // array for saving data
                DefaultTableModel tbModel = (DefaultTableModel)tblRooms.getModel();

                tbModel.addRow(tbData);
            
        }
        }
        catch(Exception ex){
            {
                JOptionPane.showMessageDialog(null, "Error Connecting to Database"+ex,"View Stocks Form",
                    JOptionPane.ERROR_MESSAGE);
            }
        }
             
    }
    
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     
     boolean valid=true;
     String roomNo= txtRnumber.getText();
     String roomtype=(String)cmbRtype.getSelectedItem();
     String bed=(String)cmbBed.getSelectedItem();
     String price=txtPrice.getText();
     
      if(txtPrice.getText().isEmpty()||txtRnumber.getText().isEmpty()||cmbRtype.getSelectedItem().toString().isEmpty()||cmbRtype.getSelectedItem().toString().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Fields can't be blank", "Manage Room Form", JOptionPane.ERROR_MESSAGE);
            valid=false;
        }
      

              
     if(valid)
            {
                try
                {
                   PreparedStatement stat=con.prepareStatement("Insert into Rooms values ('"+roomNo+"','"+roomtype+"','"+bed+"','"+price+"','Not Booked')");
                    int rows=stat.executeUpdate();
            
            if(rows>0)
            {
                JOptionPane.showMessageDialog(null,"Room details have been saved");
                update (); 
            }
          
        }
        
        catch(Exception ex)
        {
          JOptionPane.showMessageDialog(null,"Room Exists",
                  "Room Manage Form",JOptionPane.ERROR_MESSAGE);
        } 
        
            }
              
    
    }//GEN-LAST:event_jButton2ActionPerformed

