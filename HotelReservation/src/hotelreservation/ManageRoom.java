    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

             boolean valid=true;

     
      if(txtPrice.getText().isEmpty()||txtRnumber.getText().isEmpty()||cmbRtype.getSelectedItem().toString().isEmpty()||cmbRtype.getSelectedItem().toString().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Fields can't be blank", "Manage Room Form", JOptionPane.ERROR_MESSAGE);
            valid=false;
        }
     if(valid)
           
        try
        {
            PreparedStatement stat=con.prepareStatement("Update Rooms set RoomType=?, Bed=?, Price=? where RoomNumber=?");

            
            stat.setString(1,(String)cmbRtype.getSelectedItem());
            stat.setString(2,(String)cmbBed.getSelectedItem());
            stat.setString(3,txtPrice.getText());
            stat.setString(4,txtRnumber.getText());

            int rows=stat.executeUpdate();
            if(rows>0)
            {
                JOptionPane.showMessageDialog(null,"Room Updated..");
                update (); 
            }

        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,"Room not Updated Successully:"+ex,"Manage Room Form",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
                          try
        {

            PreparedStatement stmt=con.prepareStatement("Select roomNumber,RoomType,Bed,Price  from Rooms where RoomNumber=?");

            stmt.setString(1,txtRnumber.getText());

            ResultSet rs=stmt.executeQuery();

            if(rs.next())
            {
              
                txtRnumber.setText(rs.getString("roomNumber"));
                cmbRtype.setSelectedItem(rs.getString("RoomType")); 
                cmbBed.setSelectedItem(rs.getString("Bed")); 
                txtPrice.setText(rs.getString("Price"));
    
                
                JOptionPane.showMessageDialog(null,"Room Found");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Room not Found.");
            }
        }

        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,"Error Encounted while searching:"+ex,"Room Form",JOptionPane.ERROR_MESSAGE);
        }
                          
                          
    }//GEN-LAST:event_jButton4ActionPerformed

